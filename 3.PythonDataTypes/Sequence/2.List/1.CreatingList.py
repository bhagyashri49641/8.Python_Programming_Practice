# Python program to demonstrate
# Creation of List

# Creating a List
List = []
print("Blank List: ")
print(List)

# Creating a List of numbers
List = [1, 4, 6]
print("\nList of numbers: ")
print(List)

# Creating a List of strings and accessing using index
List = ["Hello", "Hi", "Bye"]
print("\nList Items: ")
print(List[0])
print(List[2])

# Creating a Multi-Dimensional List
# (By Nesting a list inside a List)
List = [['Hello', 'Hi'] , ['Bye']]
print("\nMulti-Dimensional List: ")
print(List)


##################################################################################

# Creating a List withthe use of Numbers (Having duplicate values)
List = [1, 2, 4, 4, 3, 3, 3, 6, 5]
print("\nList with the use of Numbers: ")
print(List)

# Creating a List with mixed type of values (Having numbers and strings)
List = [1, 2, 'Hello', 4, 'For', 6, 'Hello']
print("\nList with the use of Mixed Values: ")
print(List)

