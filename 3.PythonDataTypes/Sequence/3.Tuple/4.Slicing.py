# Slicing of a Tuple

# Slicing of a Tuple with Numbers
Tuple1 = tuple('WelcomeToTheWorldOfPython')
print(Tuple1)

# Printing elements of a Range 
print("Printing elements between Range 4 to 9: ")
print(Tuple1[4:9])

# Reversing the Tuple
print("\nTuple after sequence of Element is reversed: ")
print(Tuple1[::-1])

# Printing elements of a Range with negative index
print("\nPrinting elements between Range -3 to -8: ")
print(Tuple1[-8:-3])

